<h2>| 最新法規公告 |</h2>

  <div><p>一、提高酒駕違規罰鍰：
若是喝酒開車初犯，原先處以罰緩 1 萬 9 千 5 百元到 9 萬，新制提高為 3 萬到 12 萬，而機車則是維持原本 1 萬 5 千元到 9 萬元，但兩者若是累犯則加重處罰， 5 年內第 2 次酒駕即處以最高罰鍰，第 3 次就再按次增加 9 萬元。</p></div>

<div><p>二、拒絕酒測加重處罰
>若是喝酒開車、騎車遇到臨檢拒絕酒測，罰鍰從 9 萬元提高到 18 萬元， 5 年內第 2 次後每次加重 18 萬元，且每次拒測後都應吊銷駕照。</p></div>

<div><p>三、酒駕吊扣駕照：若喝酒遭到吊扣駕照，原訂 3 年後就可重考，但新制重考期限雖ㄧ樣，但必須加裝「酒精鎖」違者罰 6 千元至 1 萬 2 千元，預計明年 3 月將全面實施，以目前規定來看，機車初犯吊扣駕照 1 年、汽車吊扣 2 年，車上如有未滿 12 歲兒童或者肇事致人受傷，吊扣駕照 2 到 4 年。</p></div>

<div><p>四、酒駕連坐罰：若與酒駕者同車，駕駛酒測超過 0.25 的話，同車 18 歲以上、 70 歲以下，都會被罰 6 百元至 3 千元，不過排除年紀太長、心智障礙、或搭乘計程車、公車等運輸業乘客之處罰，千萬不要認為自己沒喝酒就沒事。</p></div>
	
